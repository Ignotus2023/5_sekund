function build(): Record<Tier, Prompt[]> {
  const out: Partial<Record<Tier, Prompt[]>> = {};
  (Object.keys(RAW) as Tier[]).forEach((tier) => {
    out[tier] = RAW[tier].map((entry, i) => {
      const [text, category] = entry;
      return { id: `${tier}-${i}`, text, tier, category };
    });
  });
  return out as Record<Tier, Prompt[]>;
}

export const PROMPTS: Record<Tier, Prompt[]> = build();

export function countByTier(): Record<Tier, number> {
  const out: Partial<Record<Tier, number>> = {};
  (Object.keys(PROMPTS) as Tier[]).forEach((t) => (out[t] = PROMPTS[t].length));
  return out as Record<Tier, number>;
}

export function countByCategoryAndTier(): Record<CategoryKey, Record<Tier, number>> {
  const result: Partial<Record<CategoryKey, Record<Tier, number>>> = {};
  (Object.keys(PROMPTS) as Tier[]).forEach((tier) => {
    PROMPTS[tier].forEach((p) => {
      const cat = p.category;
      if (!result[cat]) result[cat] = {} as Record<Tier, number>;
      result[cat]![tier] = (result[cat]![tier] || 0) + 1;
    });
  });
  return result as Record<CategoryKey, Record<Tier, number>>;
}
